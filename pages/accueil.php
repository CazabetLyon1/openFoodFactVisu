    <p>
      <b>Simplifier l'étiquetage nutritionnel sur l'emballage pour aider les consommateurs à choisir des aliments plus sains, c'est le but du système de notation avec 5 couleurs proposé par le Professeur Serge Hercberg du Programme National Nutrition et Santé.</b>
    </p>
    <p class="text-cnter">
      </br></br><img src="/RC2/data/classification.png" class="text-center" /></br></br>
    </p>
    <p>
        Dans le même esprit que l'étiquette énergie des appareils électroménagers, ce système prévoit l'attribution d'une note de A à E (chacune associée à une couleur de vert à rouge) qui serait affichée de manière volontaire sur la face avant des produits. Ce système fait partie du projet de loi Santé de Marisol Touraine qui sera débattu à l'Assemblée nationale le 31 mars 2015.</br></br>
    </p>
    <p>
      <b>Plus de fruits et légumes, de protéines et de fibres, moins de calories, de sucre, de graisses saturées et de sel.</b></p>
    <p>
      "5 fruits et légumes par jour", "Moins gras, moins sucré, moins salé" : les notes de couleur tentent de faire la synthèse de ces messages bien connus du grand public mais pas forcément faciles à appliquer lorsque l'on fait ses courses.
      Pour établir la note d'un aliment, on calcule d'abord un score nutritionnel qui tient compte de la teneur en éléments jugés favorables sur le plan nutritionnel (les fruits et légumes, les protéines et les fibres) et en éléments jugés défavorables (l'énergie, les sucres simples, les acides gras saturés, et le sodium correspondant au sel). Les notes couleurs vert, jaune, orange, rose fuchsia et rouge (A, B, C, D, E) correspondent à différents intervalles du score nutritionnel.</br></br>
    </p>

    <p>
      <b>Une note pour comparer les aliments lors de ses achats</b>
    </p>

    <p>
      À la différence d'autres systèmes de notation (comme les pourcentages d'apports journaliers recommandés), la note est indépendante de la portion indiquée par le fabriquant, souvent différente selon les marques et parfois sous-estimée par rapport à la portion effectivement consommée.
      Ne pas lier la note à la taille de la portion permet de comparer les notes de produits différents avant de choisir lequel acheter.
    </p>
